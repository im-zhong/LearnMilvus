# On Milvus, you can create multiple collections to manage your data, and insert your data as entities into the collections.
# Collection and entity are similar to tables and records in relational databases.
# A collection is a two-dimensional table with fixed columns and variant rows
# Each column represents a field, and each row represents an entity.


